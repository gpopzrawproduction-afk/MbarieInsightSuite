using System.Collections.ObjectModel;
using CommunityToolkit.Mvvm.ComponentModel;
using CommunityToolkit.Mvvm.Input;
using MediatR;
using Microsoft.Extensions.Logging;
using MIC.Core.Application.Notifications.Commands.Dismiss;
using MIC.Core.Application.Notifications.Commands.MarkAllRead;
using MIC.Core.Application.Notifications.Commands.MarkRead;
using MIC.Core.Application.Notifications.Queries.GetNotifications;
using MIC.Core.Domain.Entities;
using MIC.Desktop.Avalonia.Services;

namespace MIC.Desktop.Avalonia.ViewModels;

/// <summary>
/// ViewModel for the notifications slide-in panel.
/// Manages notification state, filtering, and actions.
/// </summary>
public partial class NotificationsPanelViewModel : ViewModelBase
{
    private readonly IMediator _mediator;
    private readonly ILogger<NotificationsPanelViewModel> _logger;

    public NotificationsPanelViewModel(
        IMediator mediator,
        ILogger<NotificationsPanelViewModel> logger)
    {
        _mediator = mediator;
        _logger = logger;
        GroupedNotifications = new ObservableCollection<NotificationGroupDto>();
    }

    // ??????????????????????????????????????????????????????????????????????
    // PANEL STATE
    // ??????????????????????????????????????????????????????????????????????

    [ObservableProperty]
    private bool isPanelOpen;

    partial void OnIsPanelOpenChanged(bool value)
    {
        if (value)
        {
            LoadNotificationsCommand.Execute(null);
        }
    }

    // ??????????????????????????????????????????????????????????????????????
    // FILTER
    // ??????????????????????????????????????????????????????????????????????

    [ObservableProperty]
    private NotificationType? activeFilter; // null = ALL

    partial void OnActiveFilterChanged(NotificationType? value)
    {
        LoadNotificationsCommand.Execute(null);
    }

    // ??????????????????????????????????????????????????????????????????????
    // STATE
    // ??????????????????????????????????????????????????????????????????????

    [ObservableProperty]
    private bool isLoading;

    [ObservableProperty]
    private bool hasNotifications;

    [ObservableProperty]
    private int unreadCount;

    [ObservableProperty]
    private bool hasUnread;

    // ??????????????????????????????????????????????????????????????????????
    // DATA
    // ??????????????????????????????????????????????????????????????????????

    public ObservableCollection<NotificationGroupDto> GroupedNotifications { get; }

    // ??????????????????????????????????????????????????????????????????????
    // LOAD NOTIFICATIONS
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand]
    private async Task LoadNotificationsAsync()
    {
        IsLoading = true;

        try
        {
            var query = new GetNotificationsQuery
            {
                FilterType = ActiveFilter,
                IncludeRead = true,
                PageSize = 50
            };

            var result = await _mediator.Send(query);

            if (result.IsError)
            {
                _logger.LogWarning("Failed to load notifications: {Error}", result.FirstError.Description);
                IsLoading = false;
                return;
            }

            UnreadCount = result.Value.UnreadCount;
            HasUnread = UnreadCount > 0;
            HasNotifications = result.Value.Notifications.Any();

            // Group by date label
            GroupedNotifications.Clear();
            var grouped = result.Value.Notifications
                .GroupBy(n => GetDateGroup(n.CreatedAt))
                .Select(g => new NotificationGroupDto
                {
                    GroupLabel = g.Key,
                    Items = g.ToList()
                });

            foreach (var group in grouped)
            {
                GroupedNotifications.Add(group);
            }

            _logger.LogInformation("Loaded {Count} notifications, {UnreadCount} unread",
                result.Value.Notifications.Count, UnreadCount);
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "Error loading notifications");
        }
        finally
        {
            IsLoading = false;
        }
    }

    private static string GetDateGroup(DateTime date)
    {
        var today = DateTime.Today;
        var diff = today - date.Date;

        return diff.Days == 0 ? "TODAY"
             : diff.Days == 1 ? "YESTERDAY"
             : diff.Days <= 7 ? "THIS WEEK"
             : "EARLIER";
    }

    // ??????????????????????????????????????????????????????????????????????
    // MARK SINGLE AS READ
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand]
    private async Task MarkReadAsync(NotificationDto? notification)
    {
        if (notification == null || notification.IsRead)
            return;

        var result = await _mediator.Send(
            new MarkNotificationReadCommand { NotificationId = notification.Id });

        if (!result.IsError)
        {
            UnreadCount = Math.Max(0, UnreadCount - 1);
            HasUnread = UnreadCount > 0;
            await LoadNotificationsAsync();
            _logger.LogInformation("Notification marked as read: {NotificationId}", notification.Id);
        }
    }

    // ??????????????????????????????????????????????????????????????????????
    // MARK ALL AS READ
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand(CanExecute = nameof(HasUnread))]
    private async Task MarkAllReadAsync()
    {
        var userId = UserSessionService.Instance.CurrentUserId ?? string.Empty;

        if (string.IsNullOrEmpty(userId))
        {
            _logger.LogWarning("Cannot mark all as read: user not authenticated");
            return;
        }

        var result = await _mediator.Send(
            new MarkAllNotificationsReadCommand { UserId = userId });

        if (!result.IsError)
        {
            UnreadCount = 0;
            HasUnread = false;
            await LoadNotificationsAsync();
            _logger.LogInformation("All notifications marked as read for user: {UserId}", userId);
        }
    }

    // ??????????????????????????????????????????????????????????????????????
    // DISMISS NOTIFICATION
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand]
    private async Task DismissAsync(NotificationDto? notification)
    {
        if (notification == null)
            return;

        var result = await _mediator.Send(
            new DismissNotificationCommand { NotificationId = notification.Id });

        if (!result.IsError)
        {
            // Remove from grouped list
            foreach (var group in GroupedNotifications.ToList())
            {
                var item = group.Items.FirstOrDefault(n => n.Id == notification.Id);
                if (item != null)
                {
                    group.Items.Remove(item);
                    if (!group.Items.Any())
                    {
                        GroupedNotifications.Remove(group);
                    }
                    break;
                }
            }

            HasNotifications = GroupedNotifications.Any(g => g.Items.Any());
            _logger.LogInformation("Notification dismissed: {NotificationId}", notification.Id);
        }
    }

    // ??????????????????????????????????????????????????????????????????????
    // SET FILTER
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand]
    private void SetFilter(NotificationType? filter)
    {
        ActiveFilter = filter;
    }

    // ??????????????????????????????????????????????????????????????????????
    // CLOSE PANEL
    // ??????????????????????????????????????????????????????????????????????

    [RelayCommand]
    private void ClosePanel()
    {
        IsPanelOpen = false;
    }

    // ??????????????????????????????????????????????????????????????????????
    // TOGGLE (public method for header button)
    // ??????????????????????????????????????????????????????????????????????

    public void Toggle()
    {
        IsPanelOpen = !IsPanelOpen;
    }
}

/// <summary>
/// DTO for grouping notifications by date.
/// </summary>
public class NotificationGroupDto
{
    public string GroupLabel { get; set; } = string.Empty;
    public List<NotificationDto> Items { get; set; } = new();
}
